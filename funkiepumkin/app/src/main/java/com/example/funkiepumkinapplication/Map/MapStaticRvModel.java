package com.example.funkiepumkinapplication.Map;

public class MapStaticRvModel {
    private int image;
    private String text;

    public MapStaticRvModel(int image, String text) {
        this.image = image;
        this.text = text;
    }

    public int getImage() {
        return image;
    }

    public String getText() {
        return text;
    }
}
